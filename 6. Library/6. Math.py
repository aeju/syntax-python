# math : 수학적인 기능(팩토리얼, 제곱근, 최대공약수)


# 1. factorial(x) : x!
import math

print(math.factorial(5)) # 5 팩토리얼을 출력


# 2. sqrt(x) : x의 제곱근
import math

print(math.sqrt(7))


# 3. gcd(a, b) : 최대 공약수
import math

print(math.gcd(21, 14))


# 4. 상수(pi, e)
import math

print(math.pi) # 파이 출력
print(math.e) # 자연상수 e
